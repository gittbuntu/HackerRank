

# isalnum(), isdigit(), islower(), isupper(), isalpha()
s = input()
if s.isalnum():
    print(True)
else:
    print(False)

if s.isdigit():
    print(True)
else:
    print(False)

if s.islower():
    print(True)
else:
    print(False)
if s.isupper():
    print(True)
else:
    print(False)
if s.isalpha():
    print(True)
else:
    print(False)
